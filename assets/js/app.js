//Este programa esta utilizando un API de GitHub para obtener y mostraar los siguiente datos del usuario: (nombre del usuario, blog y localizacion)

const baseEndpoint = 'https://api.github.com';
const usersEndpoint = `${baseEndpoint}/users`;
const $n = document.querySelector('.name');
const $b = document.querySelector('.blog');
const $l = document.querySelector('.location');
//faltaba un . a variables de querySelector

//se agrega async 
async function displayUser(username) {
  $n.textContent = 'cargando...';
  const response = await fetch(`${usersEndpoint}/${username}`);
  const data = await response.json();
  //7manda error, se cambian comillas simples a ``
  $n.textContent = `${data.name}`;
  $b.textContent = `${data.blog}`;
  $l.textContent = `${data.location}`;
}

function handleError(err) {
  console.log('OH NO!');
  console.log(err);
  n.textContent = `Algo sali√≥ mal: ${err}`
}

displayUser('stolinski').catch(handleError);